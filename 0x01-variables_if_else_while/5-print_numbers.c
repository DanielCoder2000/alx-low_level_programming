#include<stdio.h>
/**
 * main - Prints "all the number of base10
 * ascendingly" then plints a new line
 *
 * Return: Always 0.
 */

int main(void)
{
int a;

for (a = 0; a < 10; a++)
{
printf("%d", a);
}
printf("\n");

return (0);
}
